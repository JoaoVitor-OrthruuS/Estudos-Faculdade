function calcularMais(valor1, valor2) {
    const resultado = valor1 + valor2;
    return resultado;
}

function calcularMenos(valor1, valor2) {
    const resultado = valor1 - valor2;
    return resultado;
}

function calcularMult(valor1, valor2) {
    const resultado = valor1 * valor2;
    return resultado;
}

function calcularDiv(valor1, valor2) {
    const resultado = valor1 / valor2;
    return resultado;
}
